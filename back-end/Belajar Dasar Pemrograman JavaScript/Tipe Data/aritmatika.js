let a = 12;
let b = 9;

console.log(`${a} + ${b}: ${a + b}`); // Penjumlahan
console.log(`${a} - ${b}: ${a - b}`); // Pengurangan
console.log(`${a} * ${b}: ${a * b}`); // Perkalian
console.log(`${a} / ${b}: ${a / b}`); // Pembagian
console.log(`${a} % ${b}: ${a % b}`); // Sisa Bagi
console.log(`${a} ** ${b}: ${a ** b}`); // Perpangkatan
